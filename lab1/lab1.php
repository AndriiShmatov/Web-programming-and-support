<?php
// Виведення на екран тексту "Hello, World!"
echo "Hello, World!<br>";

// Оголошення змінних різних типів
$stringVar = "Це рядок";   // рядкова змінна
$intVar = 123;             // ціле число
$floatVar = 12.34;         // число з плаваючою комою
$boolVar = true;           // булеве значення

// Виведення значень змінних на екран
echo "Рядок: $stringVar<br>";
echo "Ціле число: $intVar<br>";
echo "Число з плаваючою комою: $floatVar<br>";
echo "Булеве значення: " . ($boolVar ? 'true' : 'false') . "<br>";

// Виведення типу кожної змінної за допомогою var_dump
echo "Типи змінних:<br>";
var_dump($stringVar);
echo "<br>";
var_dump($intVar);
echo "<br>";
var_dump($floatVar);
echo "<br>";
var_dump($boolVar);
echo "<br>";

// Конкатенація рядків
$string1 = "Привіт, ";
$string2 = "світ!";
$concatenatedString = $string1 . $string2;  // Об'єднання рядків
echo "Об'єднаний рядок: $concatenatedString<br>";

// Створюємо змінну з числовим значенням
$number = 7; 

// Перевірка на парність за допомогою if-else
if ($number % 2 == 0) {
    echo "Число $number є парним.<br>";
} else {
    echo "Число $number є непарним.<br>";
}

// Виведення чисел від 1 до 10 за допомогою циклу for
echo "Числа від 1 до 10:<br>";
for ($i = 1; $i <= 10; $i++) {
    echo $i . " ";
}
echo "<br>";

// Виведення чисел від 10 до 1 за допомогою циклу while
echo "Числа від 10 до 1:<br>";
$j = 10;
while ($j >= 1) {
    echo $j . " ";
    $j--;
}
echo "<br>";

// Створюємо асоціативний масив студента
$student = [
    "ім'я" => "Олександр",
    "прізвище" => "Іваненко",
    "вік" => 20,
    "спеціальність" => "Програмування"
];

// Виведення значень кожного елемента масиву
echo "Інформація про студента:<br>";
echo "Ім'я: " . $student["ім'я"] . "<br>";
echo "Прізвище: " . $student["прізвище"] . "<br>";
echo "Вік: " . $student["вік"] . "<br>";
echo "Спеціальність: " . $student["спеціальність"] . "<br>";

// Додаємо новий елемент до масиву - середній бал
$student["середній бал"] = 4.8;

// Виведення оновленого масиву
echo "Оновлена інформація про студента:<br>";
foreach ($student as $key => $value) {
    echo "$key: $value<br>";
}

?>
